public class NOK {
    
    private String name;
    private int mobile;
    
    public NOK(){}

    public NOK(String name, int mobile) {
        this.name = name;
        this.mobile = mobile;
    }
   
    public String getName() {
        return name;
    }
    
    public int getMobile(){
        return mobile;
    }

    
}
